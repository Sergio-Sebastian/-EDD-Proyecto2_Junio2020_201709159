    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!nombrestxt.getText().equals("") && !apellidostxt.getText().equals("") && !licenciatxt.getText().equals("") && !generotxt.getText().equals("") && !fechatxt.getText().equals("") && !teltxt.getText().equals("") && !dirtxt.getText().equals("")){
            NodoLCD nuevo = new NodoLCD(Long.parseLong(dpicond), nombrestxt.getText(), apellidostxt.getText(), licenciatxt.getText(), generotxt.getText(), fechatxt.getText(), teltxt.getText(), dirtxt.getText());
            Objetos.Conductores.BuscarNodo(dpicond).setDatos(nuevo);
            JOptionPane.showMessageDialog(null, "Modificaci√≥n completa");
        }else{
            JOptionPane.showMessageDialog(null, "Hay campos vacios");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


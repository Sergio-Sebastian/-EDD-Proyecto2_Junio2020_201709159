    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtBoxAddVacias()) {
        } else {
            try {
                long dpi = Long.parseLong(txtdpi.getText());
                long tel = Long.parseLong(txttel.getText());
                if (dpi < 0 || tel < 0) {
                    JOptionPane.showMessageDialog(null, "Ingrese números positivos en 'DPI' y 'Telefono'", "ATENCIÓN", 1);
                } else if (miTablaUsuarios.DPIRegistrado(dpi)) {
                    JOptionPane.showMessageDialog(null, "Este número de DPI ya esta registrado", "ATENCIÓN", 1);
                } else {
                    Cliente nvoCliente = new Cliente(dpi, txtnombre.getText(), txtapellido.getText(), 
                            txtgenero.getText(), txtfecha.getText(), tel, txtdir.getText());
                    
                    miTablaUsuarios.insertar(nvoCliente);
                    JOptionPane.showMessageDialog(null, "Registro Exitoso!", "Completado", 1);
                    limpiar1();
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Debes ingresar un número en 'DPI' y en 'Telefono'");
            } catch (Exception ed) {
                JOptionPane.showMessageDialog(null, " - ERROR, intenta mas tarde "+ed+" - ", "ERROR", 1);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    public void limpiar1() {
        txtdpi.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtgenero.setText("");
        txtfecha.setText("");
        txttel.setText("");
        txtdir.setText("");
    }
    
    public boolean txtBoxAddVacias() {
        if (txtdpi.getText().equals("") || txtnombre.getText().equals("") || txtapellido.getText().equals("")
                || txtgenero.getText().equals("") || txtfecha.getText().equals("") || txttel.getText().equals("") 
                || txtdir.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe Completar la Información Correpondiente", "ATENCIÓN", 1);
            return true;
        }
        return false;
    }
    


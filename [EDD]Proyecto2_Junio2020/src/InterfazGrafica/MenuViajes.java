    public void LlenarTabla() {
        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        Object columnas[] = new Object[7];
        NodoBlockChain aux = Viajes.Cabeza;
        if (aux != null) {
            while (aux != Viajes.Cola && aux != null) {
                columnas[0] = aux.Llave;
                columnas[1] = aux.Origen;
                columnas[2] = aux.Destino;
                columnas[3] = aux.FechaHora;
                //columnas[4] = aux.Cliente;
                columnas[5] = aux.Conductor.Nombres;
                //columnas[6] = aux.Vehiculo;
                tabla.addRow(columnas);
                aux = aux.Siguiente;
            }
            if (aux == Viajes.Cola && aux != null) {
                columnas[0] = aux.Llave;
                columnas[1] = aux.Origen;
                columnas[2] = aux.Destino;
                columnas[3] = aux.FechaHora;
                //columnas[4] = aux.Cliente;
                columnas[5] = aux.Conductor.Nombres;
                //columnas[6] = aux.Vehiculo;
                tabla.addRow(columnas);
            }
        }

    }

